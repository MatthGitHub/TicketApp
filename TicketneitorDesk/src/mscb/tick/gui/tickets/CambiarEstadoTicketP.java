    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if(cmbx_estados.getSelectedItem().equals(miTick.getUltimoEstado())){
            
        }else{
            HistorialServ servH = HistorialServ.getHistorialServ();
            HistorialTickets his = new HistorialTickets();
            Date fecha = new Date();
            his.setFkEstado((Estados) cmbx_estados.getSelectedItem());
            his.setFecha(fecha);
            his.setHora(fecha);
            his.setFkTicket(miTick);
            his.setFkUsuario(LoginEJB.usuario);
            his.setResolucion(miTick.getResolucion());
            servH.nuevo(his);
            TicketsV tickete = TicketsV.getTickets(mainFrameO);
            tickete.llenarTabla();
            btn_cerrarActionPerformed(evt);
        }
    }//GEN-LAST:event_btn_guardarActionPerformed


