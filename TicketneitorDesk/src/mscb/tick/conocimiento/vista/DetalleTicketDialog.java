    public void setearValoresTicket(BaseConocimiento miBase){
        DateFormat dateFormatter;
        dateFormatter = DateFormat.getDateInstance(DateFormat.SHORT, Locale.US);
        txtNumTicket.setText(miBase.getFkTicket().getIdTicket().toString());
        txtFecha.setText(dateFormatter.format(miBase.getFkTicket().getFecha()));
        txtUsuarioE.setText(miBase.getFkTicket().getFkUsuarioEmisor().getFkEmpleado().getNombre()+" "+miBase.getFkTicket().getFkUsuarioEmisor().getFkEmpleado().getApellido());
        txtUsuarioR.setText(miBase.getFkTicket().getUsuarioReceptor().getFkEmpleado().getNombre()+" "+miBase.getFkTicket().getUsuarioReceptor().getFkEmpleado().getApellido());
        txtAObs.setText(miBase.getFkTicket().getObservacion());
        txtAResp.setText(miBase.getFkTicket().getRespuesta());
        txtAReso.setText(miBase.getResolucion());
    }
