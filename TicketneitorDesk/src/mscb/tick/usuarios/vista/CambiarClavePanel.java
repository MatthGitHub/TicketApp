    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        servicios = new UsuarioServ();
        if(pswr_actual.getText().equals(LoginEJB.usuario.getContrasenia())){
            if(pswr_nueva.getText().equals(pswr_nueva2.getText())){
                if(servicios.cambiarClave(LoginEJB.usuario, pswr_nueva.getText())== 0){
                    JOptionPane.showMessageDialog(estePanel, "Clave modificada");
                    this.setVisible(false);
                    estePanel = null;
                    mainFrame.setVisible(false);
                    mainFrame = null;
                    System.gc();
                }
            }else{
                JOptionPane.showMessageDialog(estePanel, "Las claves nuevas no coinciden");
            }
        }else{
            JOptionPane.showMessageDialog(estePanel, "Clave actual incorrecta");
        }
        
    }//GEN-LAST:event_btn_guardarActionPerformed


